              <div class="space20">&nbsp;</div>
              <p>Loại cây này đầu tiên chỉ được trồng ở châu Phi và Ả Rập, nhưng sau được đem phân bố ở nhiều nơi khác trên thế giới với điều kiện hợp phong thổ. Người Hà Lan đem phổ biến việc canh tác cà phê đến các xứ thuộc địa của họ. Thống đốc Van Hoorn cho trồng cà phê trên đảo Tích Lan (Sri Lanka ngày nay) vào năm 1690 (có tài liệu ghi là năm 1658), rồi sau du nhập sang đảo Java (Indonesia) năm 1696 (hoặc 1699). Năm 1710 thương gia Âu châu đem cây cà phê về và trồng thử trong các khu vườn sinh vật ở Âu châu. Amsterdam là nơi đầu tiên cây cà phê nảy mầm trên lục địa châu Âu. Năm 1718 người Hà Lan mang cây cà phê tới Surinam, rồi năm 1725 thì người Pháp mang đem trồng ở Cayenne, 1720/1723 và Martinique v.v. Sang cuối thế kỷ 18 cây cà phê đã được trồng ở khắp các xứ sở nhiệt đới, chủ yếu do sự bành trướng thuộc địa của các đế quốc Âu châu. Đồn điền cà phê đầu tiên được lập ở Việt Nam là do người Pháp khởi sự ở gần Kẻ Sở, Bắc Kỳ vào năm 1888. Giống cà phê arabica (tức cà phê chè) được trồng ở ven sông. Sau việc canh tác cà phê lan xuống vùng Phủ Lý, Ninh Bình, Thanh Hóa, Nghệ An, Kon Tum và Di Linh. Năm 1937-1938 tổng cộng trên lãnh thổ Việt Nam có 13.000 ha cà phê, cung ứng 1.500 tấn. Hiện tại,Việt Nam có ba loại cà phê chính, đó là cà phê chè (arabica), cà phê vối (robusta), cà phê mít (lyberica).
